    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nome = jTValueNome.getText();
        String email = jTValueEmail.getText();
        String password = jPassword.getText();
        String telephone = jTTelephone.getText();
        double nota1 = Double.parseDouble(jTNota1.getText());
        double nota2 = Double.parseDouble(jTNota2.getText());
        
        double media = (nota1 + nota2)/2;
        
        switch (jComboBox1.getSelectedIndex()){
            case 1: 
            if (media >= 7){
                JOptionPane.showMessageDialog(null, "Aprovado"); 
            }else if (media < 7 && media >= 5){
                JOptionPane.showMessageDialog(null, "Recuperação");
            }else if (media < 5){    
                JOptionPane.showMessageDialog(null,"Reprovado");
            }
            JOptionPane.showMessageDialog(null, "Nome: "+ nome +" email: "
                    + email+" password "+password +"telephone:"+telephone +" - Graduação");
            break;
            
            case 2: 
            if (media >= 6){
                JOptionPane.showMessageDialog(null, "Aprovado"); 
            }else if (media < 6 && media >= 4){
                JOptionPane.showMessageDialog(null, "Recuperação");
            }else if (media < 4){    
                JOptionPane.showMessageDialog(null,"Reprovado");
            }
            JOptionPane.showMessageDialog(null, "Nome: " + nome + " email: " 
                    + email + " password " + password + " telephone: " + telephone + " - Pós-Graduação");
            break;
            
            case 3:
            if (media >= 5){
                JOptionPane.showMessageDialog(null, "Aprovado"); 
            }else if (media < 5 && media >= 3){
                JOptionPane.showMessageDialog(null, "Recuperação");
            }else if (media < 3){    
                JOptionPane.showMessageDialog(null,"Reprovado");
            }
            JOptionPane.showMessageDialog(null, "Nome: " + nome + " email: " 
                    + email + " password " + password + " telephone: " + telephone + " - Doutorado");
            break;
            default :
            JOptionPane.showMessageDialog(null, "Selecione o tipo de curso");
    }//GEN-LAST:event_jButton3ActionPerformed
    }
